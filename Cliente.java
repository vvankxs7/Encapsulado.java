public class Cliente{
	private String nome;
	private float credito;

	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getNome() {
		return this.nome;
	}
	public void setCredito(float credito) {
		this.credito = credito;
	}
	public float getCredito() {
		return this.credito;
	}
}
